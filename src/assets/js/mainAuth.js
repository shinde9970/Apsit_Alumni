
(function ($) {
    "use strict";

    /*==================================================================
    [ Validate ]*/
    var input = $('.validate-input .input100');

    $('.validate-form').on('submit',function(){
        var check = true;

        for(var i=0; i<input.length; i++) {
            if(validate(input[i]) == false){
                showValidate(input[i]);
                check=false;
            }
        }

        return check;
    });

    


    $('.validate-form .input100').each(function(){
        $(this).focus(function(){
           hideValidate(this);
        });
    });

    function validate (input) {
        if($(input).attr('type') == 'email' || $(input).attr('name') == 'email') {
            if($(input).val().trim().match(/^([a-zA-Z0-9_\-\.]+)@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.)|(([a-zA-Z0-9\-]+\.)+))([a-zA-Z]{1,5}|[0-9]{1,3})(\]?)$/) == null) {
                return false;
            }
        }
        else {
            if($(input).val().trim() == ''){
                return false;
            }
        }
    }

    function showValidate(input) {
        var thisAlert = $(input).parent();

        $(thisAlert).addClass('alert-validate');
    }

    function hideValidate(input) {
        var thisAlert = $(input).parent();

        $(thisAlert).removeClass('alert-validate');
    }
    
    /*==================================================================
    [ Show pass ]*/
    var showPass = 0;
    $('.btn-show-pass').on('click', function(){
        if(showPass == 0) {
            $(this).next('input').attr('type','text');
            $(this).find('i').removeClass('fa-eye');
            $(this).find('i').addClass('fa-eye-slash');
            showPass = 1;
        }
        else {
            $(this).next('input').attr('type','password');
            $(this).find('i').removeClass('fa-eye-slash');
            $(this).find('i').addClass('fa-eye');
            showPass = 0;
        }
        
    });

    

    /* ====================================================================
    password match for signup page */
    function checkPasswordMatch(){
        const password = $("#password").val();
        const confirmPassword = $("#confirmPassword").val();
        if (password != confirmPassword)
            $("#passwordInfo").html("<i class='fas fa-exclamation-circle text-danger' aria-hidden='true'></i> <small class='text-danger' >Passwords do not match!</small>");
        else
            $("#passwordInfo").html("<i class='fas fa-check-circle text-success' aria-hidden='true'></i> <small class='text-success' >Passwords matched.</small>");
    }
    $("#password, #confirmPassword").keyup(checkPasswordMatch);
    /* password match for signup page end  */

    $(".toast").toast('show');
    $(".close-toast").on("click", function() {
        $(".toast").parent("div").remove()
    });
    

})(jQuery);

